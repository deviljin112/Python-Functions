# Create a function that takes in two arguments as ints
# and adds them together
def addition(val_1, val_2):
    return val_1 + val_2


# Create a function that takes in two arguments as ints
# and subtracts them from each other
def subtraction(val_1, val_2):
    return val_1 - val_2


## Task 1 ##
# Create a function for multiplication
def multiply(val_1, val_2):
    return val_1 * val_2


## Task 2 ##
# Create a function for division
def divide(val_1, val_2):
    return val_1 / val_2


## Task 3 ##
# Create a function for modulus
def modulo(val_1, val_2):
    return val_1 % val_2


## Task 4 ##
# Create a function to take a number to the power of another
def power(val_1, val_2):
    return val_1 ** val_2
